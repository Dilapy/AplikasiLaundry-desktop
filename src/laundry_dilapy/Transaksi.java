    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        // TODO add your handling code here:
        model = new DefaultTableModel(null,new Object[] {"ID TRANSAKSI", "KODE INVOICE", "ID MEMBER", "TGL", "BATAS WAKTU", "TANGGAL BAYAR", "BIAYA TAMBAHAN", "DISKON", "STATUS", "DIBAYAR", "ID PAKET", "QTY"});
        tabel_transaksi.setModel(model);
        model.getDataVector().removeAllElements();
        try {
            PreparedStatement s = k.getCon().prepareStatement("SELECT *from transaksi where dibayar LIKE '%"+ txtcari.getText().toString()+"%' or status LIKE '%"+ txtcari.getText().toString() +"%'");
            ResultSet rs = s.executeQuery();
            while(rs.next()){
                Object[] data ={
                    rs.getString("id_transaksi"),
                    rs.getString("kode_invoice"),
                    rs.getString("id_member"),
                    rs.getString("tgl"),
                    rs.getString("batas_waktu"),
                    rs.getString("tgl_bayar"),
                    rs.getString("biaya_tambahan"),
                    rs.getString("diskon"),
                    rs.getString("status"),
                    rs.getString("dibayar"),
                    rs.getString("id_paket"),
                    rs.getString("qty")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.print("ERROR KUERI KE DATABASE:\n" + e + "n\n");
        }
    }//GEN-LAST:event_cariActionPerformed

