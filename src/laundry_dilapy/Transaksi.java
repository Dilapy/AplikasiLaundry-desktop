    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String inv = "";
        Date d = new Date();
        DateFormat dateformat = new SimpleDateFormat("YYYY-MM-dd");
        DateFormat dateformat2 = new SimpleDateFormat("YYYY-MM-dd");
        inv = dateformat.format(d);
        double diskon = Double.parseDouble(cb_diskon.getSelectedItem().toString()) /100;
        String[] split = cb_paket.getSelectedItem().toString().split(":");
        int total = Integer.parseInt(txtqty.getText()) * Integer.parseInt(split[2]);
        int pajak = hitungPajak(total);
        
        try {
            this.stat = k.getCon().prepareStatement("INSERT INTO transaksi VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            stat.setInt(1, 0);
            stat.setString(2, txtidoutlet.getText());
            stat.setString(3, inv);
            stat.setString(4, cb_idmember.getSelectedItem().toString());
            stat.setString(5, dateformat.format(tgl.getDate()));
            stat.setString(6, dateformat2.format(bataswaktu.getDate()));
            stat.setString(7, null);
            stat.setString(8, txtbiayatambah.getText());
            stat.setDouble(9, diskon);
            stat.setInt(10, pajak);
            stat.setString(11, "Proses");
            stat.setString(12, "Belum Dibayar");
            stat.setString(13, txtiduser.getText());
            stat.setString(14, split[0]);
            stat.setString(15, txtqty.getText());
            stat.setString(16, txtket.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data telah tersimpan");
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_saveActionPerformed

