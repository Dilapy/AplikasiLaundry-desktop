    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        try {
            this.stat = k.getCon().prepareStatement("INSERT INTO member VALUES (?,?,?,?,?)");
            stat.setInt(1, 0);
            stat.setString(2, txtnama.getText());
            stat.setString(3, txtalamat.getText());
            stat.setString(4, cb_jk.getSelectedItem().toString());
            stat.setString(5, txttelp.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data telah tersimpan");
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_saveActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        try {
            this.stat = k.getCon().prepareStatement("UPDATE member SET nama=?, alamat=?, jenis_kelamin=?, tlp=? WHERE id_member=?");
            stat.setString(1, txtnama.getText());
            stat.setString(2, txtalamat.getText());
            stat.setString(3, cb_jk.getSelectedItem().toString());
            stat.setString(4, txttelp.getText());
            stat.setString(5, txtid.getText());
            stat.executeUpdate();
            refreshTable();
            JOptionPane.showMessageDialog(this, "Data telah di update");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_editActionPerformed

